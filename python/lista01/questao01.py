A = float(input())
B = float(input())

print(str(A) + " + " + str(B) + " = " + str(A+B))
print(str(A) + " - " + str(B) + " = " + str(A-B))
print(str(A) + " / " + str(B) + " = " + str(A/B))
print(str(A) + " // " + str(B) + " = " + str(int(A)//int(B)))
print(str(A) + " % " + str(B) + " = " + str(int(A)%int(B)))